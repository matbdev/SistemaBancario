    private void cbCorrentistaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbCorrentistaActionPerformed
        Pessoa correntista = (Pessoa) this.cbCorrentista.getSelectedItem();

        if (correntista != null) {
            this.tfCPF.setText(correntista.getCpfFormatado());
            this.tfNome.setText(correntista.getNome());
            this.tfEndereco.setText(correntista.getEndereco());
        }
        else {
            this.tfCPF.setText("");
            this.tfNome.setText("");
            this.tfEndereco.setText("");
        }
    }//GEN-LAST:event_cbCorrentistaActionPerformed

