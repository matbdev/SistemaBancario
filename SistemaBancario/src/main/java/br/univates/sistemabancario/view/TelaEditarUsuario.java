    private void carregarCorrentistas(Pessoa correntista){
        model.removeAllElements(); 
        model.addElement(null);

        for (Pessoa p : this.cdao.readAll()) {
            model.addElement(p);
        }
        
        if (correntista != null) {
            model.setSelectedItem(correntista);
        }
    }

